package Projeto03TrabalhandoComTiposDeDados;

public class App {
    public static void main(String[] args) {
        System.out.println("Aprendendo POO com Java");

        byte tipoByte = 127;
        Short tipoShort = 32767;
        Integer tipoInteger = 2147483647;
        Long tipoLong = 9223372036854775807L;
        Float tipoFloat = 3.4028235E38F;
        Double tipoDouble = 1.7976931348623157E308;
        String tipoString = "Sou um texto";
        char tipoChar = 'S';
        Boolean tipoBoolean = false;

        System.out.println(tipoByte);
        System.out.println(tipoShort);
        System.out.println(tipoInteger);
        System.out.println(tipoLong);
        System.out.println(tipoFloat);
        System.out.println(tipoDouble);
        System.out.println(tipoString);
        System.out.println(tipoChar);
        System.out.println(tipoBoolean);

    }
}